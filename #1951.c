int main(){
	int badok[19][19] = {0};
	int n,i,j,x,y;
	int x_y[2];
	
	scanf("%d",&n);
	
	
	for(i=0 ; i<n ; i++)
	{
		scanf("%d%d",&x_y[0],&x_y[1]);
		x=x_y[0]-1;
		y=x_y[1]-1;
		badok[x][y] = 1;
	}
	
	for(i=0;i<20;i++)
    {
    	for(j=0; j<20 ; j++)
    	{
    		printf("%d ",badok[i][j]);
    		if(j==19) printf("\n");
		}
	}
}
